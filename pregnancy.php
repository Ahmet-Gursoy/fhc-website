                        İKİNCİ ÜÇ AYDAKİ FİZİKSEL DEĞİŞİKLİKLER ve SIKÇA SORULANLAR
                        <br><br>
                        • Bursa ve yanmalar <br>
                        • Cilt değişiklikleri (gebelik lekelenmeleri, cilt döküntüleri, sivilcelenmeler) <br>
                        • Kabızlık <br>
